import { DynamoDBClient } from "@aws-sdk/client-dynamodb";
import { DynamoDBDocumentClient, PutCommand } from "@aws-sdk/lib-dynamodb";
import {
  ImportActorInput,
  ImportActorOutput,
  Response,
  ImportActorInputDynamo,
} from "../../types";

const client = new DynamoDBClient({});
const dynamo = DynamoDBDocumentClient.from(client);

export const importActor = async (
  input: Required<ImportActorInput>
): Promise<Response<ImportActorOutput>> => {

  const {
    id,
    firstName,
    lastName,
  } = input;

  const t = "actor";
  const v = "LATEST";
  const cd = new Date().toISOString();
  const ud = new Date().toISOString();
  const fn = firstName;
  const ln = lastName;
  const pk = id.toLowerCase();
  const sk = t.toLowerCase() + "#" + v.toLowerCase();
  const idx = fn.toLowerCase() + "" + ln.toLowerCase();

  const result = await dynamo.send(
    new PutCommand({
      TableName: "tenant",
      Item: {
        id,
        t,
        v,
        cd,
        ud,
        fn,
        ln,
        pk,
        sk,
        idx,
      } as ImportActorInputDynamo,
    })
  );

};
